foods = ['bacon', 'tuna', 'ham', 'sausages', 'beef']  # array of items

# colon with a number means the program will start at the beginning, and ends at a point you want it to end
for f in foods[:2]:  # the letter f is a variable name
    print(f)  # indented together means it will be part of the loop
    print(len(f))  # will print out numbers after strings
